package chap02;

public class literalExam01 {

	public static void main(String[] args) {
		int var1 = 10;
		System.out.println(var1);
		
		int var2 = 010;
		System.out.println(var2); //1x8**1, 0X8**0

		int var3 = 0x10;
		System.out.println(var3); // 1x16**1, 0x16**0
		
		double var4 = 0.35;
		System.out.println(var4);
		
		int var5 = 300;
		System.out.println(var5);
		
	
		double var6 = 3E2;
		System.out.println(var6);
		
	}

}
